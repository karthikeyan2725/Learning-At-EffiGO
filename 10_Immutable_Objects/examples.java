public class examples {
    public static void main(String args[]){
        // Integer Immutable Objects
        int a = 5;
        Integer b = 5;

        System.out.println(a);
        System.out.println(b);

        // Character;
        char primitiveChar = 'c';
        Character immutableChar = 'c';

        System.out.println(primitiveChar);
        System.out.println(immutableChar);

        // Long
        Long immutableLong = 12490293203203L;
        System.out.println(immutableLong);
        
        // String 
        String string = "Hello";
        System.out.println(string);
        
    }
}
